// Generator for API endpoints

/**
 * Generate AI context header for API endpoint files
 */
function generateAIHeader(data, operation) {
  const { plural, layer, layerPascalCase, pascalCasePlural } = data
  const apiPath = `${layer}-${plural}`
  const prefixedPascalCasePlural = `${layerPascalCase}${pascalCasePlural}`

  const operationDescriptions = {
    get: 'GET all items or by IDs (?ids=)',
    post: 'CREATE new item',
    patch: 'UPDATE item by ID',
    delete: 'DELETE item by ID'
  }

  return `/**
 * @crouton-generated
 * @collection ${plural}
 * @layer ${layer}
 * @operation ${operation.toUpperCase()}
 * @generated ${new Date().toISOString().split('T')[0]}
 *
 * ## AI Context
 * - Endpoint: /api/teams/[id]/${apiPath}
 * - Operation: ${operationDescriptions[operation]}
 * - Queries: ${prefixedPascalCasePlural} functions from database/queries
 * - Auth: Team membership required
 *
 * ## Common Modifications
 * - Add validation: Use readValidatedBody with Zod schema
 * - Add filtering: Extend getQuery() parsing
 * - Add permissions: Check user role in membership
 * - Add hooks: Emit custom events after operation
 *
 * Generated by @friendlyinternet/nuxt-crouton-collection-generator
 * Safe to modify - regeneration requires --force flag
 */

`
}

export function generateGetEndpoint(data, config = null) {
  const { pascalCase, pascalCasePlural, layerPascalCase, plural, singular, layer } = data
  const prefixedPascalCase = `${layerPascalCase}${pascalCase}`
  const prefixedPascalCasePlural = `${layerPascalCase}${pascalCasePlural}`

  // Check if this collection has translations
  const hasTranslations = config?.translations?.collections?.[plural] || config?.translations?.collections?.[singular]

  const queriesPath = '../../../../database/queries'

  // Generate AI context header
  const aiHeader = generateAIHeader(data, 'get')

  return `${aiHeader}import { getAll${prefixedPascalCasePlural}, get${prefixedPascalCasePlural}ByIds } from '${queriesPath}'
import { eq, and } from 'drizzle-orm'
import * as tables from '@@/server/database/schema'

export default defineEventHandler(async (event) => {
  const { id: teamSlugOrId } = getRouterParams(event)
  const { user } = await requireUserSession(event)

  // Resolve team by slug or ID
  let team = await useDB()
    .select()
    .from(tables.teams)
    .where(eq(tables.teams.slug, teamSlugOrId))
    .get()

  // If not found by slug, try by ID
  if (!team) {
    team = await useDB()
      .select()
      .from(tables.teams)
      .where(eq(tables.teams.id, teamSlugOrId))
      .get()
  }

  if (!team) {
    throw createError({ statusCode: 404, statusMessage: 'Team not found' })
  }

  // Check if user is member of team
  const membership = await useDB()
    .select()
    .from(tables.teamMembers)
    .where(
      and(
        eq(tables.teamMembers.teamId, team.id),
        eq(tables.teamMembers.userId, user.id)
      )
    )
    .get()

  if (!membership) {
    throw createError({ statusCode: 403, statusMessage: 'Unauthorized' })
  }

  const query = getQuery(event)${hasTranslations ? `
  // Accept locale for future translation handling
  const locale = String(query.locale || 'en')` : ''}
  if (query.ids) {
    const ids = String(query.ids).split(',')
    return await get${prefixedPascalCasePlural}ByIds(team.id, ids)
  }

  return await getAll${prefixedPascalCasePlural}(team.id)
})`
}

