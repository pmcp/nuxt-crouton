const addBlockedDate = () => {
  if (!state.value.blockedDates) state.value.blockedDates = []
  state.value.blockedDates = [
    ...state.value.blockedDates,
    { id: nanoid(), startDate: '', endDate: '', reason: '', blockedSlots: [] }
  ]
}

