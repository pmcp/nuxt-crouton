#!/usr/bin/env bash
# Post the rendered digest as a comment on the single standing "Daily epic digest"
# issue, creating that issue the first time if it doesn't exist. Auth via gh + GH_TOKEN.
#
#   GH_TOKEN=... DIGEST_BODY_FILE=digest.md bash post-comment.sh
set -euo pipefail

REPO="${DIGEST_REPO:-pmcp/nuxt-crouton}"
TITLE="${DIGEST_ISSUE_TITLE:-📊 Daily epic digest}"
BODY_FILE="${DIGEST_BODY_FILE:-digest.md}"

# Find the standing issue via the REST list API (immediately consistent). NOT
# `gh issue list --search` — the search index lags right after the issue is created,
# which made the very first run create the issue but then fail to re-find it.
find_issue() {
  gh issue list --repo "$REPO" --state open --label meta:agents --limit 500 \
    --json number,title --jq ".[] | select(.title==\"$TITLE\") | .number" | head -n1
}

num="$(find_issue || true)"

if [ -z "${num:-}" ]; then
  echo "No standing issue found — creating it."
  # Capture the new issue's URL from create itself → no re-find, no search lag.
  created="$(gh issue create --repo "$REPO" --title "$TITLE" --label meta:agents --body \
"Standing home for the **daily epic digest**. Each morning the \`epic-digest\` workflow posts a fresh comment here — a running log of where every open epic stands plus what moved in the last 24h.

Generated by \`.github/workflows/epic-digest.yml\` (deterministic, no LLM). Don't close this issue.")"
  num="$(printf '%s' "$created" | grep -oE '[0-9]+$' | tail -n1)"
fi

if [ -z "${num:-}" ]; then
  echo "Could not find or create the standing issue." >&2
  exit 1
fi

echo "Posting digest comment to #$num"
gh issue comment "$num" --repo "$REPO" --body-file "$BODY_FILE"
