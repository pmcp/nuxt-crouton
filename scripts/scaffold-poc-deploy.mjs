#!/usr/bin/env node
// scaffold-poc-deploy.mjs — generate a POC's Cloudflare Workers preview deploy.
//
// Part of epic #265 ("build X → testable preview URL"). Given a poc under pocs/<name>,
// it writes .github/workflows/deploy-<name>.yml: a thin caller of the reusable
// deploy-app.yml (workspace: pocs) that, on every PR touching the poc, deploys an
// isolated, auto-provisioned Cloudflare Workers *staging* env and posts the preview
// URL on the PR. Staging-only and safe-to-fail — promote to apps/ for production.
//
// Usage:  node scripts/scaffold-poc-deploy.mjs <name>
//         pnpm poc:scaffold-deploy <name>
//
// The deployed preview is stable at https://<name>.pmcp.dev (the #133 staging domain),
// so BETTER_AUTH_URL is predictable and auth works on the preview. The poc's
// wrangler.jsonc (env.staging route + id-less D1) is produced by the app scaffold;
// this script only emits the workflow and sanity-checks the wrangler.

import { existsSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const name = process.argv[2]

if (!name || !/^[a-z][a-z0-9-]*$/.test(name)) {
  console.error('Usage: node scripts/scaffold-poc-deploy.mjs <name>   (lowercase, kebab-case)')
  process.exit(1)
}

const pocDir = resolve(repoRoot, 'pocs', name)
if (!existsSync(pocDir)) {
  console.error(`✗ pocs/${name} does not exist — scaffold the POC app first (it lives in pocs/, not apps/).`)
  process.exit(1)
}

// Sanity-check the wrangler.jsonc has a staging env (built by the app scaffold).
const wranglerPath = resolve(pocDir, 'wrangler.jsonc')
if (!existsSync(wranglerPath)) {
  console.warn(`⚠ pocs/${name}/wrangler.jsonc missing — the app scaffold should create it (id-less D1 + env.staging route ${name}.pmcp.dev).`)
} else {
  const w = readFileSync(wranglerPath, 'utf8')
  if (!/"staging"\s*:/.test(w)) {
    console.warn(`⚠ pocs/${name}/wrangler.jsonc has no env.staging block — staging deploy needs one (route "${name}.pmcp.dev", id-less ${name}-staging-db).`)
  }
}

const Title = name.replace(/(^|-)([a-z])/g, (_, d, c) => (d ? ' ' : '') + c.toUpperCase()).trim()

const workflow = `name: Deploy ${Title} (POC preview)

# AUTO-GENERATED by scripts/scaffold-poc-deploy.mjs (epic #265). Do not hand-edit;
# re-run the generator to update.
#
# A pocs/ preview: opening/updating a PR that touches this POC deploys an isolated,
# auto-provisioned Cloudflare Workers *staging* env (its own D1/KV) and posts the
# preview URL on the PR. Stable at https://${name}.pmcp.dev so auth works.
# Safe-to-fail — staging only, no production. Promote to apps/ for a prod deploy.

on:
  pull_request:
    paths:
      - 'pocs/${name}/**'
      - '.github/workflows/deploy-${name}.yml'
      - '.github/workflows/deploy-app.yml'
  workflow_dispatch:

jobs:
  deploy:
    # The reusable workflow's PR-comment step needs pull-requests: write; a caller
    # must grant it explicitly under restrictive default token permissions.
    permissions:
      contents: read
      pull-requests: write
    uses: ./.github/workflows/deploy-app.yml
    with:
      app: ${name}
      workspace: pocs
      environment: staging
      staging-url: https://${name}.pmcp.dev
      layer-packages: '@fyit/crouton-core @fyit/crouton-auth @fyit/crouton-i18n @fyit/crouton'
    secrets: inherit
`

const outDir = resolve(repoRoot, '.github', 'workflows')
mkdirSync(outDir, { recursive: true })
const outPath = resolve(outDir, `deploy-${name}.yml`)
const existed = existsSync(outPath)
writeFileSync(outPath, workflow)

console.log(`${existed ? '↻ updated' : '✓ created'} .github/workflows/deploy-${name}.yml`)
console.log(`  → on PRs touching pocs/${name}/**, deploys staging Workers and comments the preview URL.`)
console.log(`  → preview will be https://${name}.pmcp.dev (auth-working; needs CLOUDFLARE_* CI secrets + pmcp.dev zone).`)
