#!/usr/bin/env node
/**
 * gen-package-catalog.mjs — regenerate the internal package catalog the
 * task-decomposition pipeline reads for its "package-fit check" (step 0, #292).
 *
 * The orchestrator must, before slicing a "build X" epic into workstreams, check
 * whether an existing @fyit/crouton-* package already does most of the work — so it
 * proposes building ON a package instead of reinventing it (the #274 blog run hand-rolled
 * a posts collection + reader when crouton-pages + crouton-editor already cover a blog).
 * This turns that check into ONE file read instead of 28 package CLAUDE.md reads.
 *
 *   node scripts/gen-package-catalog.mjs            # write the catalog
 *   node scripts/gen-package-catalog.mjs --check    # CI: fail if out of date
 *
 * Source of truth: each packages/<name>/package.json (name + description). The generated
 * table is committed at .claude/skills/task-decompose/package-catalog.md. Keep it fresh by
 * re-running after adding/renaming a package (a CI check can enforce --check).
 */

import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const pkgDir = resolve(root, 'packages')
const outPath = resolve(root, '.claude/skills/task-decompose/package-catalog.md')

function firstClaudeLine(dir) {
  // A richer one-liner than package.json sometimes gives: the first real prose line of
  // the package's CLAUDE.md (skipping the title heading and blank lines). Best-effort.
  const p = resolve(dir, 'CLAUDE.md')
  if (!existsSync(p)) return ''
  const lines = readFileSync(p, 'utf8').split('\n')
  for (const raw of lines) {
    const l = raw.trim()
    if (!l || l.startsWith('#') || l.startsWith('>') || l.startsWith('<!--')) continue
    return l.replace(/\s+/g, ' ').slice(0, 160)
  }
  return ''
}

const rows = []
for (const name of readdirSync(pkgDir).sort()) {
  const dir = resolve(pkgDir, name)
  const pkgJson = resolve(dir, 'package.json')
  if (!existsSync(pkgJson)) continue
  let manifest
  try { manifest = JSON.parse(readFileSync(pkgJson, 'utf8')) } catch { continue }
  if (manifest.private === true && !manifest.name) continue
  const pkgName = manifest.name || `(packages/${name})`
  // "What it provides": package.json description, falling back to the CLAUDE.md one-liner.
  const provides = (manifest.description || firstClaudeLine(dir) || '—').replace(/\|/g, '\\|')
  rows.push({ dir: name, pkgName, provides })
}

const header = `<!-- GENERATED by scripts/gen-package-catalog.mjs — do not edit by hand. Re-run after adding/renaming a package. -->

# Internal package catalog (package-fit check — #292)

The task-orchestrator's **Step 0 package-fit check** scans this table before slicing a
"build X" epic: if an existing \`@fyit/crouton-*\` package already covers a meaningful chunk
of the ask, it proposes building **on** that package (Option A) vs from scratch (Option B),
defaulting to reuse. This is the *internal* sibling of the \`ecosystem-check\` skill (external
OSS). Drill into \`packages/<dir>/CLAUDE.md\` for detail once a candidate is spotted.

| Package | Dir | What it provides (when to reach for it) |
|---------|-----|------------------------------------------|
`
const body = rows.map(r => `| \`${r.pkgName}\` | \`packages/${r.dir}\` | ${r.provides} |`).join('\n')
const out = `${header}${body}\n`

const isCheck = process.argv.includes('--check')
const current = existsSync(outPath) ? readFileSync(outPath, 'utf8') : ''
if (isCheck) {
  if (current !== out) {
    console.error('✗ package-catalog.md is out of date — run `node scripts/gen-package-catalog.mjs` and commit.')
    process.exit(1)
  }
  console.log('✓ package-catalog.md is up to date.')
  process.exit(0)
}
writeFileSync(outPath, out)
console.log(`✓ wrote ${outPath} (${rows.length} packages)`)
